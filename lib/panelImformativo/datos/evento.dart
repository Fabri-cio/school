class Evento {
  final String titulo;
  final String descripcion;
  final DateTime fechaHora;
  final String lugar;
  final String organizador;
  final String imagen;

  Evento({
    required this.titulo,
    required this.descripcion,
    required this.fechaHora,
    required this.lugar,
    required this.organizador,
    required this.imagen,
  });
}
